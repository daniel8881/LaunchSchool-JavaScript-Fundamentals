/*
  Primitives value in JavaScript are immutable,such as Numbers, Strings, and boolean values, when
  we call toUppercase on a string, it will return a new string instead to mutate the original 
  string.

  String literals (denoted by double or single quotes) and strings returned from String calls
  in a non-constructor context (i.e., without using the new keyword) are primitive strings. 
  JavaScript automatically converts primitives to String objects, so that it's possible to use 
  String object methods for primitive strings as well as Number and Boolean.
*/